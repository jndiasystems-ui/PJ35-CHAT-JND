/* ─── Motor de i18n ─── */
const i18n = (() => {
  let currentLang = localStorage.getItem('jnd_lang') || 'es';

  function t(key) {
    return JND_TRANSLATIONS[currentLang]?.[key]
        || JND_TRANSLATIONS['es']?.[key]
        || key;
  }

  function apply(lang) {
    currentLang = lang;
    localStorage.setItem('jnd_lang', lang);
    document.documentElement.setAttribute('lang', lang);

    // Actualizar todos los elementos con data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = t(key);
      if (val) el.innerHTML = val;
    });

    // Actualizar placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      const val = t(key);
      if (val) el.setAttribute('placeholder', val);
    });

    // Actualizar botón de idioma
    const btn = document.getElementById('langToggleBtn');
    if (btn) {
      btn.querySelector('.lang-active').textContent  = lang.toUpperCase();
      btn.querySelector('.lang-other').textContent   = lang === 'es' ? 'EN' : 'ES';
    }
  }

  function toggle() {
    apply(currentLang === 'es' ? 'en' : 'es');
  }

  function init() {
    apply(currentLang);
  }

  return { apply, toggle, init, t, get current() { return currentLang; } };
})();
